!                   **************************
                    SUBROUTINE USER_TOM_CORFON
!                   **************************
!
!
!***********************************************************************
! TOMAWAC
!***********************************************************************
!
!brief    USER MODIFIES THE BOTTOM TOPOGRAPHY.
!
!history  F. MARCOS
!+
!+
!+
!
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!
      USE BIEF
      USE DECLARATIONS_TOMAWAC
!
      USE DECLARATIONS_SPECIAL
      USE INTERFACE_TOMAWAC, EX_USER_TOM_CORFON => USER_TOM_CORFON
      IMPLICIT NONE
!
!-----------------------------------------------------------------------
!
      INTEGER K
!
!-----------------------------------------------------------------------
!
      DO K=1,NPOIN2
        IF (ZF(K).LT.-10.D0) THEN
          ZF(K)=ZF(K)
        ELSE
          ZF(K)=-10.D0
        ENDIF
      ENDDO
!
!-----------------------------------------------------------------------
!
      RETURN
      END
