!                   **************************
                    SUBROUTINE USER_ART_CORFON
!                   **************************
!
!
!***********************************************************************
! ARTEMIS   V6P1                                   21/08/2010
!***********************************************************************
!
!brief    MODIFIES THE BOTTOM TOPOGRAPHY.
!
!history  J-M HERVOUET
!+        01/03/1990
!+        V5P1
!+
!
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!
      USE BIEF
      USE DECLARATIONS_ARTEMIS
!
      USE DECLARATIONS_SPECIAL
      IMPLICIT NONE
!
!+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
!
!
!+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
!
      INTEGER I
      DOUBLE PRECISION :: RCP,BCP,XCP,DCP,YCP
!
!-----------------------------------------------------------------------
!
!     Variables
      RCP  = 1.
      BCP  = 0.4*RCP
!      HCP  = BCP/0.3
!
!     Circle centre
      XCP = 6.*RCP
      YCP = 10.*RCP
!
!     Elliptical obstacle
      DO I = 1,NPOIN
        DCP=( (X(I)-XCP)**2. + (Y(I)-YCP)**2. )**0.5
!
        IF ( DCP.LT.RCP ) THEN
          ZF%R(I) = BCP*( 1. - (DCP/RCP)**2. )
        ELSE
          ZF%R(I) = 0.D0
        ENDIF
      ENDDO
!
!-----------------------------------------------------------------------
!
      RETURN
      END
